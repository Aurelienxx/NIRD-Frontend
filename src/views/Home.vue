        <h2>La démarche NIRD</h2>

        <p>La <b>démarche NIRD</b> (pour un Numérique Inclusif, Responsable et Durable) propose aux <b>établissements scolaires</b> un cheminement progressif en <b>trois jalons</b>.</p>

        <p>Elle commence par une phase de <b>mobilisation</b>, où un enseignant volontaire initie la réflexion et sensibilise l’équipe éducative, avec le soutien de la direction et une mise en réseau avec d’autres établissements. 
