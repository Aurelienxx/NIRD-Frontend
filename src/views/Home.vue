        <h2>La démarche NIRD pour les collectivités</h2>

        <p>La démarche NIRD propose aux collectivités (communes, départements, régions) un cadre structuré pour déployer un numérique responsable et durable dans les établissements scolaires, en trois étapes clés : mobilisation (sensibilisation des acteurs, cartographie du parc informatique, désignation d’un référent), expérimentation (tests pilotes avec des distributions Linux adaptées, reconditionnement d’équipements, formation des équipes), et intégration (généralisation d’une offre Linux officielle, institutionnalisation des processus, insertion dans les stratégies locales).</p>

        <p>Portée par une logique d’économie circulaire, de souveraineté numérique et d’inclusion, cette approche permet de transformer les parcs vieillissants en ressources pédagogiques pérennes, tout en impliquant élèves, enseignants et partenaires locaux dans une dynamique collaborative et écologique.</p>
        
      
        <h2>Jalon 1 – Mobilisation</h2>

        <ul>
          <li>Objectif : Sensibiliser la collectivité (commune, département, région) aux enjeux du numérique responsable et initier un cadre de travail partagé avec les établissements dont elle a la charge.</li>
          <li>Actions clés :</li>
          <ul>
            <li>Nomination d’un·e référent·e NIRD au sein de la collectivité, qui devient l’interlocuteur privilégié des écoles, collèges ou lycées, ainsi que des académies (DRANE, DRASI) et le ministère (DNE).</li>
            <li>Session d’information interne à destination des élus, DSI, services éducation, services techniques (enjeux du numérique responsable et durable, souveraineté numérique, économie circulaire des équipements, usages pédagogiques de Linux, exemples de démarches réussies dans les autres collectivités).</li>
            <li>Première cartographie du parc informatique (âge des équipements, compatibilité Windows 11, flux de renouvellement, contraintes réseau, capacités locales de maintenance ou de reconditionnement).</li>
            <li>Information aux établissements scolaires pour repérer les volontaires et les besoins.</li>
            <li>Mise en réseau avec la communauté NIRD : participation aux forums Tchap de la démarche, à des webinaires, échanges avec d’autres collectivités engagées.</li>
            <li>Prise de contact avec les DRANE et les DRASI pour préparer accompagnement et formation et cadrer les aspects techniques.</li>
          </ul>
          <li>Résultat attendu : Une collectivité sensibilisée, un référent identifié, des écoles/collèges/lycées intéressés, et une vision initiale du potentiel de déploiement (et de reconditionnement) de Linux dans les établissements.</li>
        </ul>


        <h2>Jalon 2 – Expérimentation</h2>

        <ul>
          <li>Objectif : Tester, avec un groupe pilote d’établissements, la pertinence du déploiement de Linux (PrimTux au primaire, Linux NIRD au secondaire) en déployant si possible des ateliers de reconditionnement locaux.</li>
          <li>Actions clés :</li>
          <ul>
            <li>Nomination d’un·e référent·e NIRD au sein de la collectivité, qui devient l’interlocuteur privilégié des écoles, collèges ou lycées, ainsi que des académies (DRANE, DRASI) et le ministère (DNE).</li>
            <li>Sélection d’établissements pilotes (écoles, collèges, lycées). Sur la base du volontariat, de l’état du parc et de la présence d’un enseignant motivé pour devenir coordinateur NIRD.</li>
            <li>Choix de la distribution selon le niveau scolaire : PrimTux pour les écoles, et Linux NIRD pour les collèges et les lycées (à adapter et scripter pour s’intégrer dans le parc de l’établissement et le réseau mis en place par la collectivité).</li>
            <li>Tests techniques dans les établissements pilotes : intégration réseau, déploiement, test logiciels, périphériques, identifications des freins (suite bureautique MS Office, gestion du tableau numérique type SMARTBoard, client Pronote/EDT…), etc.</li>
            <li>Formation et accompagnement : formation des enseignants et agents, documentation simplifiée, séances de prise en main pour les élèves, accompagnement du coordinateur NIRD de l’établissement, suivi DRANE/ERUN/DNE, etc.</li>
            <li>Collecte des retours d’expérience : stabilité et performance, adéquation aux usages pédagogiques, charge pour les services techniques, intérêt des élèves, retour des enseignants et chefs d’établissement, analyse coûts–bénéfices, etc.</li>
            <li>Mise en place d’un dispositif local de reconditionnement : Le reconditionnement peut être effectué par les équipes techniques de la collectivités en mobilisant éventuellement des partenaires (associations locales de réemploi numérique, fablabs, étudiants, etc.). Il peut également, et c’est à encourager, se faire par les élèves eux-mêmes directement dans les établissement scolaires en créant des clubs informatique des reconditionnement (cela implique les élèves, permet de développer des compétences, comprendre la technologie pour mieux agir sur elle, etc.)</li>
          </ul>
          <li>Résultat attendu : Une expérimentation réussie dans un panel représentatif (écoles + collèges + lycées), des PC reconditionnés pour preuve de concept, et une intégration technique validée par les équipes de la collectivité.</li>
          </ul>


          <h2>Jalon 3 – Intégration</h2>

          <ul>
            <li>Objectif : Déployer une offre Linux (PrimTux, Linux NIRD…) durable, structurée et opérationnelle, proposée par la collectivité à l’ensemble des établissements sous sa responsabilité.</li>
            <li>Action clés :</li>
            <ul>
              <li>Création d’une offre Linux officielle de la collectivité comprenant : images de référence (PrimTux et Linux NIRD), documentation pour les écoles, collèges, lycées, processus de reconditionnement standardisé, modalités de maintenance et d’assistance, etc.</li>
              <li>Institutionnalisation de la chaîne de déploiement : Serveurs FOG ou équivalents sur les réseaux scolaires, intégration AD ou LDAP si existant, automatisation du PXE, gestion centralisée d’éventuels scripts, etc.</li>
              <li>Insertion du reconditionnement : Au sein de la collectivité et, si possible, généralisation de clubss informatique de reconditionnement dans les collèges et lycées,</li>
              <li>Reconnaissance des rôles : Un référent NIRD pour la collectivité, un référent NIRD dans chaque école/collège/lycée, un dialogue structuré entre les collectivités, les établissements, l’académie et le ministère.</li>
              <li>Extension progressive du parc Linux : Déploiement sur les PC non compatibles Windows 11, extension aux salles spécialisées (salle de profs, CDI, langues, techno, NSI…), usage dans les écoles sur des parcs vieillissants mais parfaitement fonctionnels sous PrimTux, réemploi des PC reconditionnés pour les élèves en difficulté (continuité pédagogique et lutte contre la fracture numérique)</li>
            </ul>
            <li>Résultat attendu : Une intégration complète, offrant aux écoles, collèges et lycées une solution Linux robuste, sécurisée, durable et soutenue par la collectivité.</li>
          </ul>

    

    


    



    

    

